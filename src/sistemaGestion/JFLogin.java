    private void logi(){
       String usu=txtUser.getText();
       String pas=new String(txtPassword.getPassword());
       Consultas kl=new Consultas();
       int a = kl.acceder(usu,pas);
       if (a==1){
           JFMenuRoot menuR= new JFMenuRoot();  
           this.setVisible(false);
           menuR.setVisible(true);
       }else if(a==2){
           JFMenuEmpleado menuE= new JFMenuEmpleado();
           this.setVisible(false);
           menuE.setVisible(true);
       }else{
          msmlogin.setText("Datos incorrectos");
       }
    }
