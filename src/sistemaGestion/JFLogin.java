    private void txtUserKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUserKeyTyped
        switch (CValidaciones.numerosYletrasMsm(evt,txtUser)) {
            case 1:
                msmusuario.setText("");
                break;
            case 2:
                msmusuario.setText("Caracter no valido");
                break;
            case 3:
                msmusuario.setText("Solo se permiten 10 caracteres");
                break;
            default:
                break;
        }
    }//GEN-LAST:event_txtUserKeyTyped

    private void txtPasswordKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPasswordKeyTyped
        //Validaciones.soloNumerosyLetras(evt,password);
        if(CValidaciones.numerosYletrasMsm(evt,txtPassword)==1){
            msmpassword.setText("");
        }else if(CValidaciones.numerosYletrasMsm(evt,txtPassword)==2){
            msmpassword.setText("Caracter no valido");
        }else if(CValidaciones.numerosYletrasMsm(evt,txtPassword)==3){
            msmpassword.setText("Solo se permiten 10 caracteres");
        }
    }//GEN-LAST:event_txtPasswordKeyTyped

